// Transforms drizzle-kit's emitted SQL files into a single TypeScript module that
// inlines every migration as a plain string. Metro does not need to know how to
// load `.sql` and drizzle's `expo-sqlite/migrator.migrate()` consumes the resulting
// `{ migrations: Record<string, string> }` directly.
//
// Run via `pnpm --filter @termlnk/database-mobile run drizzle:generate` — that
// script chains `drizzle-kit generate` and this transformer.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const migrationsRoot = join(here, '..', 'src', 'migrations');

const tagDirs = readdirSync(migrationsRoot, { withFileTypes: true })
  .filter((entry) => entry.isDirectory() && entry.name !== 'meta')
  .map((entry) => entry.name)
  .sort();

if (tagDirs.length === 0) {
  console.error('[build-migrations] No migration directories found under src/migrations.');
  process.exit(1);
}

const entries = tagDirs.map((tag) => {
  const sql = readFileSync(join(migrationsRoot, tag, 'migration.sql'), 'utf8');
  return `  ${JSON.stringify(tag)}: ${JSON.stringify(sql)}`;
});

const body = `/**
 * Copyright 2026-present Termlnk
 *
 * Licensed under the PolyForm Noncommercial License 1.0.0 (the "License");
 * you may not use this file except in compliance with the License.
 * You may obtain a copy of the License at
 *
 *     https://polyformproject.org/licenses/noncommercial/1.0.0
 *
 * Use of this software for any commercial purpose is prohibited.
 * The software is provided "AS IS", WITHOUT WARRANTY OR CONDITION OF ANY KIND,
 * either express or implied. See the License for the specific language
 * governing permissions and limitations under the License.
 */

// AUTO-GENERATED by scripts/build-migrations.mjs — do not edit. Re-run
// \`pnpm --filter @termlnk/database-mobile run drizzle:generate\` to regenerate
// after touching schema.
// Generated output keeps JSON.stringify formatting; style rules do not apply here.
/* eslint-disable style/indent, style/quotes, style/comma-dangle */

export default {
  migrations: {
${entries.join(',\n')}
  } as Record<string, string>,
};
`;

writeFileSync(join(migrationsRoot, 'index.generated.ts'), body);
console.log(`[build-migrations] Wrote ${tagDirs.length} migration(s) to src/migrations/index.generated.ts`);
